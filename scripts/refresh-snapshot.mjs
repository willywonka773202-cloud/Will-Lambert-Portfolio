// Refreshes data/snapshot.ts from the live GitHub API.
//
// Runs daily via .github/workflows/refresh-portfolio.yml (and on demand). The
// snapshot is the offline fallback the site uses when a live fetch is rate
// limited or unavailable, so keeping it fresh means the portfolio reflects new
// repos even before the next ISR revalidation.
//
// Usage: node scripts/refresh-snapshot.mjs
// Honors GITHUB_TOKEN for a higher rate limit and private repos.

import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const OWNER = "willywonka773202-cloud";
const DENYLIST = new Set(["will-lambert-portfolio", "unlocked-roblox"]);

const isHashName = (name) =>
  /^[0-9a-f]{16,}$/i.test(name) || /-[0-9a-f]{6,}$/i.test(name);

const isAllowed = (r) =>
  !DENYLIST.has(r.name.toLowerCase()) &&
  !isHashName(r.name) &&
  !r.fork &&
  !r.archived;

async function main() {
  const headers = {
    Accept: "application/vnd.github+json",
    "User-Agent": "lambert-labs-portfolio",
    "X-GitHub-Api-Version": "2022-11-28",
  };
  if (process.env.GITHUB_TOKEN) {
    headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  }

  const res = await fetch(
    `https://api.github.com/users/${OWNER}/repos?per_page=100&sort=pushed&type=owner`,
    { headers },
  );
  if (!res.ok) {
    console.error(`GitHub API error ${res.status}: ${await res.text()}`);
    process.exit(1);
  }

  const data = await res.json();
  const repos = data
    .filter(isAllowed)
    .sort((a, b) => (a.pushed_at < b.pushed_at ? 1 : -1))
    .map((r) => ({
      name: r.name,
      full_name: r.full_name,
      html_url: r.html_url,
      description: r.description ?? null,
      language: r.language ?? null,
      homepage: r.homepage ?? null,
      private: Boolean(r.private),
      fork: Boolean(r.fork),
      archived: Boolean(r.archived),
      size: Number(r.size ?? 0),
      pushed_at: r.pushed_at,
    }));

  const today = new Date().toISOString().slice(0, 10);
  const body = repos
    .map(
      (r) => `  {
    name: ${JSON.stringify(r.name)},
    full_name: ${JSON.stringify(r.full_name)},
    html_url: ${JSON.stringify(r.html_url)},
    description: ${JSON.stringify(r.description)},
    language: ${JSON.stringify(r.language)},
    homepage: ${JSON.stringify(r.homepage)},
    private: ${r.private},
    fork: ${r.fork},
    archived: ${r.archived},
    size: ${r.size},
    pushed_at: ${JSON.stringify(r.pushed_at)},
  },`,
    )
    .join("\n");

  const file = `import type { GhRepo } from "./types";

/**
 * Committed fallback snapshot of the real GitHub repos, captured from the
 * GitHub API. Used only when a live fetch fails (rate limit, network, or
 * GitHub downtime) so the site never renders empty or breaks the build.
 *
 * Auto-generated by scripts/refresh-snapshot.mjs — do not edit by hand.
 */
export const snapshotGeneratedAt = ${JSON.stringify(today)};

export const repoSnapshot: GhRepo[] = [
${body}
];
`;

  const here = dirname(fileURLToPath(import.meta.url));
  const out = join(here, "..", "data", "snapshot.ts");
  await writeFile(out, file, "utf8");
  console.log(`Wrote ${repos.length} repos to data/snapshot.ts (generated ${today}).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
